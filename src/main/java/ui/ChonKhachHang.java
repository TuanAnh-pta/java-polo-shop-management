    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonActionPerformed
        // TODO add your handling code here:
        int row = tblKhachHang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng!");
            return;
        }

        selectedKhachHang = new KhachHang();
        selectedKhachHang.setMaKhachHang(tblKhachHang.getValueAt(row, 0).toString());
        selectedKhachHang.setTenKhachHang(tblKhachHang.getValueAt(row, 1).toString());
        selectedKhachHang.setSoDienThoai(tblKhachHang.getValueAt(row, 2).toString()); // LẤY SĐT

        dispose(); // đóng dialog
        
    }//GEN-LAST:event_btnChonActionPerformed

