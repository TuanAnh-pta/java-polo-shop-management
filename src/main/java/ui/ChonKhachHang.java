      private void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        lsthang = daohang.getAll();
        for (KhachHang ha : lsthang) {
            Object[] data = {
            ha.getMaKhachHang(),
                ha.getTenKhachHang(),
                ha.getSoDienThoai(),
                ha.getEmail(),
                ha.getDiaChi()
            };
            model.addRow(data);
        }
    }
      
        private void TimKiemKhachHang(){
    String MaKhachHang = txtTimKiem.getText().trim();
    if (MaKhachHang.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã hóa đơn để tìm kiếm!");
        return;
    }
    DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
    model.setRowCount(0);
    KhachHang hang = daohang.findById(MaKhachHang);
   
    if (hang != null) {
         Object[] data ={
         hang.getMaKhachHang(),
         hang.getTenKhachHang(),
         hang.getSoDienThoai(),
         hang.getEmail(),
         hang.getDiaChi()
    };
         model.addRow(data);
    } else {
        JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn có mã: " + MaKhachHang);
    }
    }
    
     
    

