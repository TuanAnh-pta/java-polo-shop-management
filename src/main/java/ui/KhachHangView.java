    private void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblkhachhang.getModel();
        model.setRowCount(0);
        lsthang = daohang.getAll();
        for (KhachHang ha : lsthang) {
            Object[] data = {
                ha.getMaKhachHang(),
                ha.getTenKhachHang(),
                ha.getGioiTinh(),
                ha.getSoDienThoai(),
                ha.getEmail(),
                ha.getDiaChi(),
                ha.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            };
            model.addRow(data);
        }
    }

    private void setform(int index) {
        KhachHang ha = lsthang.get(index);
        txtma.setText(ha.getMaKhachHang());
        txtten.setText(ha.getTenKhachHang());
        String gioiTinh = ha.getGioiTinh();

        if ("Nam".equalsIgnoreCase(gioiTinh)) {
            rdonam.setSelected(true);
            rdonu.setSelected(false);
        } else {
            rdonam.setSelected(false);
            rdonu.setSelected(true);
        }
        txtthoai.setText(ha.getSoDienThoai());
        txtemail.setText(ha.getEmail());
        txtdiachi.setText(ha.getDiaChi());
        rdodang.setSelected(ha.isTrangThai());
        rdongung.setSelected(!ha.isTrangThai());
    }

    public KhachHang getform() {
        KhachHang ha = new KhachHang();
        ha.setMaKhachHang(txtma.getText());
        ha.setTenKhachHang(txtten.getText());
        ha.setGioiTinh(rdonam.isSelected() ? "Nam" : "Nữ");
        ha.setSoDienThoai(txtthoai.getText());
        ha.setEmail(txtemail.getText());
        ha.setDiaChi(txtdiachi.getText());
        ha.setTrangThai(true);
        return ha;
    }

    private void TimKiemTongHop() {
        String keyword = txttim.getText().trim();

        DefaultTableModel model = (DefaultTableModel) tblkhachhang.getModel();
        model.setRowCount(0);

        List<KhachHang> danhSach;

        if (keyword.isEmpty()) {
            danhSach = daohang.getAll();
        } else {
            danhSach = daohang.findAllFields(keyword);

            if (danhSach.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả phù hợp!");
                return;
            }
        }

        for (KhachHang kh : danhSach) {
            Object[] row = {
                kh.getMaKhachHang(),
                kh.getTenKhachHang(),
                kh.getGioiTinh(),
                kh.getSoDienThoai(),
                kh.getEmail(),
                kh.getDiaChi(),
                kh.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            };
            model.addRow(row);
        }
    }

    private boolean validateForm() {
        String ma = txtma.getText().trim();
        String ten = txtten.getText().trim();
        String sdt = txtthoai.getText().trim();
        String email = txtemail.getText().trim();
        String diachi = txtdiachi.getText().trim();
        if (ma.isEmpty() || ten.isEmpty() || sdt.isEmpty() || email.isEmpty() || diachi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống!");
            return false;
        }
        if (!sdt.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải đúng 10 số!");
            return false;
        }
        if (!email.matches("^[\\w.-]+@([\\w-]+\\.)+[\\w-]{2,}$")) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng!");
            return false;
        }
        for (KhachHang kh : daohang.getAll()) {
            if (kh.getMaKhachHang().equalsIgnoreCase(ma)) {
                JOptionPane.showMessageDialog(this, "Đã trùng mã khách hàng!");
                return false;
            }
        }
        int confirm = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc chắn muốn thêm khách hàng này không?",
                "Xác nhận thêm",
                JOptionPane.YES_NO_OPTION);
        return confirm == JOptionPane.YES_OPTION;
    }

    private void TimKiemTheoGioiTinh() {
        String chon = cbogioitinh.getSelectedItem().toString();
        DefaultTableModel model = (DefaultTableModel) tblkhachhang.getModel();
        model.setRowCount(0);

        if (chon.equals("Tất cả")) {
            lsthang = daohang.getAll();
        } else {
            // Truyền String thay vì boolean
            lsthang = daohang.findGioiTinh(chon);
        }

        for (KhachHang kh : lsthang) {
            Object[] data = {
                kh.getMaKhachHang(),
                kh.getTenKhachHang(),
                kh.getGioiTinh(), // Giờ là String ("Nam" / "Nữ")
                kh.getSoDienThoai(),
                kh.getEmail(),
                kh.getDiaChi(),
                kh.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            };
            model.addRow(data);
        }
    }

    

    private void delete() {
        String maKhachHang = txtma.getText().trim();
        int confirm = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc chắn muốn ngừng hoạt động khách hàng này không?",
                "Xác nhận", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            int dong = daohang.delete(maKhachHang);

            if (dong > 0) {
                JOptionPane.showMessageDialog(this, "Khách hàng đã được ngừng hoạt động!");
                fillToTable();
            }
        }
    }

    private void fillToGiaoDich(String MaKhachHang) {
        DefaultTableModel model = (DefaultTableModel) tblgiaodich.getModel();
        model.setRowCount(0);
        lstdich = (List<giaodich>) daodich.findById(MaKhachHang);
        for (giaodich d : lstdich) {
            Object[] data = {
                d.getMaKhachHang(),
                d.getMaHoaDon(),
                d.getTenSanPham(),
                d.getSoLuong(),
                d.getTongTien(),
                d.isTrangThai() ? "Đã thanh toán" : "Chưa thanh toán"
            };
            model.addRow(data);
        }
    }

