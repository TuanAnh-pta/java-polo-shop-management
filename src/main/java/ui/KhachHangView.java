    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        KhachHang kh = this.getform();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Bạn có muốn sửa thông tin khách hàng này không?",
                "Xác nhận sửa",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            KhachHang ha = this.getform();
            daohang.update(ha);
            fillToTable();
        }
        JOptionPane.showMessageDialog(this, "Sửa khách hàng thành công!");
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        String maKhachHang = txtma.getText().trim();

        if (maKhachHang.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã khách hàng cần xóa!");
            return;
        } else {
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Bạn có muốn xoá thông tin khách hàng này không?",
                    "Xác nhận xoá",
                    JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            try {
                daohang.deletehet(maKhachHang);
                JOptionPane.showMessageDialog(this, "Khách hàng đã được xoá hoàn toàn!");
                fillToTable();
            } catch (Exception e) {
                delete();
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

