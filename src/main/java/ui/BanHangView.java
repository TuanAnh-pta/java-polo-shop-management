    private void fillToTableDanhSachSP() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachSp.getModel();
        model.setRowCount(0);
        NumberFormat currencyFormat = NumberFormat.getInstance();
        lstSach = daoSach.getAll();

        for (DanhSachSp sp : lstSach) {
            SanPham sanPham = daoPham.findById(sp.getMaSanPham());
            ChatLieu chatLieu = daoLieu.finById(sp.getMaChatLieu());
            MauSac mauSac = daoSac.findById(sp.getMaMauSac());
            KichThuoc kichThuoc = daoThuoc.findById(sp.getMaKichThuoc());

            if (sanPham == null || mauSac == null || kichThuoc == null) {
                System.out.println("Dữ liệu bị thiếu với mã sản phẩm: " + sp.getMaChiTietSP());
                continue; // Bỏ qua dòng này nếu thiếu dữ liệu
            }

            Object[] data = {
                sp.getMaChiTietSP(),
                sanPham.getTenSanPham(),
                currencyFormat.format(sp.getGia()),
                chatLieu.getChatLieu(),
                mauSac.getTenMau(),
                kichThuoc.getKichThuoc(),
                sp.getSoLuong()
            };
            model.addRow(data);
        }
    }

