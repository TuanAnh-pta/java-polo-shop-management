    private void btnTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoActionPerformed
        try {
            // 1) Tạo mã hóa đơn
            String maHD = hoaDonDao.getNewMaHoaDon();
            txtMaHoaDon.setText(maHD);

            // 2) Lấy hoặc tạo khách hàng
            String tenKH = txtTenKhachHang.getText().trim();
            String maKH;

            if (tenKH.isEmpty()) {
                // KH bán lẻ
                maKH = "KHBL";
                KhachHang khBL = khachHangDao.findByMa(maKH);

                if (khBL == null) {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy KHBL!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                txtMaKhachHang.setText(khBL.getMaKhachHang());
                txtTenKhachHang.setText(khBL.getTenKhachHang());
                txtSDT.setText(khBL.getSoDienThoai());
            } else {
                // KH mới
                maKH = khachHangDao.getNewMaKhachHang();

                KhachHang kh = new KhachHang();
                kh.setMaKhachHang(maKH);
                kh.setTenKhachHang(tenKH);
                kh.setSoDienThoai(txtSDT.getText().trim());

                khachHangDao.insert(kh);
                txtMaKhachHang.setText(maKH);
            }

            // 3) Tạo hóa đơn
            HoaDon hd = new HoaDon();
            hd.setMaHoaDon(maHD);
            hd.setMaNhanVien(Session.MaNhanVien);
            hd.setMaKhachHang(maKH);
            hd.setNgayTao(new java.sql.Date(System.currentTimeMillis()));
            hd.setTrangThai(false);

            if (!hoaDonDao.add(hd)) {
                JOptionPane.showMessageDialog(this, "Tạo hóa đơn thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // ------------------------------------------------------
            // 4) TÌM ĐỢT GIẢM GIÁ ĐANG HOẠT ĐỘNG LỚN NHẤT
            // ------------------------------------------------------
            apDungDotGiamGiaChoHoaDon(maHD);

            // 5) Load lại hóa đơn
            fillHoaDon();
            JOptionPane.showMessageDialog(this, "Tạo hóa đơn thành công!");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTaoActionPerformed

