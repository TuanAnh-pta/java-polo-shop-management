    private void timKiemSanPham() {
        String tuKhoa = txtTimKiem.getText().trim();
        if (tuKhoa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã hoặc tên sản phẩm để tìm kiếm!");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblDanhSachSp.getModel();
        model.setRowCount(0);

        // Ưu tiên tìm theo mã (chính xác trước)
        DanhSachSp danhSp = daoSach.findById(tuKhoa);
        if (danhSp != null) {
            Object[] data = {
                danhSp.getMaChiTietSP(),
                daoPham.findById(danhSp.getMaSanPham()).getTenSanPham(),
                danhSp.getGia(),
                daoLieu.finById(danhSp.getMaChatLieu()).getChatLieu(),
                daoSac.findById(danhSp.getMaMauSac()).getTenMau(),
                daoThuoc.findById(danhSp.getMaKichThuoc()).getKichThuoc(),
                danhSp.getSoLuong()
            };
            model.addRow(data);
        } else {
            // Nếu không tìm thấy theo mã, tìm theo tên (danh sách nhiều kết quả)
            List<DanhSachSp> danhSachTimDuoc = daoSach.findByTenSanPham(tuKhoa); // Tạo hàm này trong DAO

            if (danhSachTimDuoc.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm có tên hoặc mã: " + tuKhoa);
            } else {
                for (DanhSachSp sp : danhSachTimDuoc) {
                    Object[] data = {
                        sp.getMaChiTietSP(),
                        daoPham.findById(sp.getMaSanPham()).getTenSanPham(),
                        sp.getGia(),
                        daoLieu.finById(sp.getMaChatLieu()).getChatLieu(),
                        daoSac.findById(sp.getMaMauSac()).getTenMau(),
                        daoThuoc.findById(sp.getMaKichThuoc()).getKichThuoc(),
                        sp.getSoLuong()
                    };
                    model.addRow(data);
                }
            }
        }
    }

