    private void cboKhuyenMaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboKhuyenMaiActionPerformed
        try {
            // 0) Tổng tiền
            String tongTienStr = txtTongTien.getText().replace(",", "").trim();
            if (tongTienStr.isEmpty()) {
                txtThanhTien.setText("0");
                txtGiamGia.setText("0");
                return;
            }
            double tongTien = Double.parseDouble(tongTienStr);

            // 1) Giảm đợt (tính chuẩn bằng BigDecimal khi cần)
            double giamDot = 0.0;
            if (dotGiamGiaHienTai != null) {
                // Chuẩn hóa kiểu LoaiGiamGia: dùng giá trị lưu trong DB, VD: "PhanTram" hoặc "TienMat"
                String loaiDot = dotGiamGiaHienTai.getLoaiGiamGia().trim();
                double giaTriDot = dotGiamGiaHienTai.getGiaTri().doubleValue();

                if (loaiDot.equalsIgnoreCase("PhanTram") || loaiDot.equals("%")) {
                    giamDot = tongTien * giaTriDot / 100.0;
                } else {
                    // Tiền mặt
                    giamDot = giaTriDot;
                }
            }

            double tienSauDot = Math.max(0, tongTien - giamDot);

            // 2) Giảm phiếu (lưu ý: KHÔNG reload danh sách trong event)
            double giamPhieu = 0.0;
            int index = cboKhuyenMai.getSelectedIndex(); // index 0 = "Không áp dụng"
            if (index > 0) {
                KhuyenMai km = danhSachKhuyenMai.get(index - 1);
                String loaiPhieu = km.getTenHinhThucGG() == null ? km.getTenHinhThucGG().trim() : km.getTenHinhThucGG().trim();
                double giaTri = km.getGiaTri();

                if (loaiPhieu.equalsIgnoreCase("Giảm theo phầm trăm") || loaiPhieu.equals("%")) {
                    giamPhieu = tienSauDot * giaTri / 100.0;
                } else {
                    giamPhieu = giaTri;
                }
            } else {
                giamPhieu = 0.0; // khi chọn "Không áp dụng" -> reset
            }

            // 3) Kết quả
            double tongGiam = giamDot + giamPhieu;
            double thanhTien = Math.max(0, tienSauDot - giamPhieu);

            txtGiamGia.setText(String.format("%,.0f", tongGiam));
            txtThanhTien.setText(String.format("%,.0f", thanhTien));

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi tính giảm giá!");
            txtThanhTien.setText("0");
            txtGiamGia.setText("0");
        }

    }//GEN-LAST:event_cboKhuyenMaiActionPerformed


