    private void btnChonSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonSanPhamActionPerformed
        // TODO add your handling code here:
        // Lấy dòng được chọn
        int selectedRow = tblDanhSachSp.getSelectedRow();
        if (selectedRow < 0 || selectedRow >= lstSach.size()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sản phẩm trong bảng!");
            return;
        }

        DanhSachSp selectedSp = lstSach.get(selectedRow);
        String maSanPham = selectedSp.getMaSanPham();

        // Nhập số lượng
        String input = JOptionPane.showInputDialog(this, "Nhập số lượng muốn mua:");
        if (input == null) {
            return; // Cancel
        }
        int soLuongMua;
        try {
            soLuongMua = Integer.parseInt(input.trim());
            if (soLuongMua <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0!");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số hợp lệ!");
            return;
        }

        // Kiểm tra tồn kho
        int soLuongHienTai = selectedSp.getSoLuong();
        if (soLuongMua > soLuongHienTai) {
            JOptionPane.showMessageDialog(this, "Số lượng vượt quá tồn kho!");
            return;
        }

        // Trừ tồn kho
        selectedSp.setSoLuong(soLuongHienTai - soLuongMua);

        // Kiểm tra giỏ hàng
        boolean daCoTrongGioHang = false;
        for (BanHang spGH : lsBanHang) {
            if (spGH.getMaSanPham().equals(maSanPham)) {
                spGH.setSoLuong(spGH.getSoLuong() + soLuongMua);
                daCoTrongGioHang = true;
                break;
            }
        }

        if (!daCoTrongGioHang) {
            List<BanHang> banHangList = banHangDao.getById(maSanPham);
            if (!banHangList.isEmpty()) {
                BanHang spMoi = banHangList.get(0);
                spMoi.setSoLuong(soLuongMua);
                lsBanHang.add(spMoi);
            }
        }

        // Cập nhật bảng giỏ hàng
        fillToTableGioHang();

        // Cập nhật bảng Danh Sách Sản Phẩm (cột "Số lượng")
        DefaultTableModel dsSpModel = (DefaultTableModel) tblDanhSachSp.getModel();
        int colIndex = tblDanhSachSp.getColumnModel().getColumnIndex("Số lượng");
        if (colIndex >= 0 && colIndex < dsSpModel.getColumnCount()) {
            dsSpModel.setValueAt(selectedSp.getSoLuong(), selectedRow, colIndex);
        }

        // Cập nhật tổng tiền và form
        updateTongTien();
        setForm(soLuongMua);

    }//GEN-LAST:event_btnChonSanPhamActionPerformed

