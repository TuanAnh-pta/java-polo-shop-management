private void doiMatKhau() {
    // Lấy tài khoản đang đăng nhập
    String taiKhoan = txtTenDangNhap.getText().trim();
    if(taiKhoan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập tên đăng nhập!");
        return;
    }

    // Panel nhập mật khẩu cũ, mới, xác nhận
    JPanel panel = new JPanel(new GridLayout(3,2,5,5));
    JPasswordField matKhauCu = new JPasswordField();
    JPasswordField matKhauMoi = new JPasswordField();
    JPasswordField xacNhan = new JPasswordField();

    panel.add(new JLabel("Mật khẩu cũ:"));
    panel.add(matKhauCu);
    panel.add(new JLabel("Mật khẩu mới:"));
    panel.add(matKhauMoi);
    panel.add(new JLabel("Xác nhận mật khẩu:"));
    panel.add(xacNhan);

    int option = JOptionPane.showConfirmDialog(this, panel, "Đổi mật khẩu", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

    if(option == JOptionPane.OK_OPTION) {
        String oldPass = new String(matKhauCu.getPassword()).trim();
        String newPass = new String(matKhauMoi.getPassword()).trim();
        String confirm = new String(xacNhan.getPassword()).trim();

        if(oldPass.isEmpty() || newPass.isEmpty() || confirm.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ các trường!");
            return;
        }

        if(!newPass.equals(confirm)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu mới không trùng khớp!");
            return;
        }

        // Kiểm tra mật khẩu cũ có đúng không
        if(!dangNhapDao.checkLogin(taiKhoan, oldPass)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu cũ không đúng!");
            return;
        }

        // Cập nhật mật khẩu mới lên DB
        boolean updateSuccess = dangNhapDao.capNhatMatKhau(taiKhoan, newPass);
        if(updateSuccess) {
            JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Đổi mật khẩu thất bại!");
        }
    }
}




