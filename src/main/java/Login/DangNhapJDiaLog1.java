    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
       String user = txtTenDangNhap.getText().trim();
    String pass = new String(txtMatKhau.getPassword());

    // 1. Kiểm tra tài khoản
    if (!dangNhapDao.checkLogin(user, pass)) {
        JOptionPane.showMessageDialog(this, 
                "Sai tài khoản hoặc mật khẩu!", 
                "Lỗi đăng nhập", 
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 2. Lấy thông tin nhân viên + chức vụ
    NhanVien nv = dangNhapDao.getNhanVienByUsername(user);
    if (nv == null) {
        JOptionPane.showMessageDialog(this, 
                "Không lấy được thông tin nhân viên!", 
                "Lỗi", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 3. Lưu session
    Session.MaNhanVien = nv.getMaNhanVien();
    Session.TenNhanVien = nv.getHoTen();
    Session.MaChucVu = nv.getMaChucVu();

    JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");

    // 4. Đóng Form đăng nhập
    this.dispose();

    // 5. CHỈ MỞ MAIN MENU
    new menumain().setVisible(true);

    }//GEN-LAST:event_btnDangNhapActionPerformed

